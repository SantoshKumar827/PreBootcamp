// Getting input via STDIN
const readline = require("readline");

const inp = readline.createInterface({
  input: process.stdin
});

const userInput = [];

inp.on("line", (data) => {
  userInput.push(data);
});

inp.on("close", () => {
  //start-here
  //Your code goes here â€¦ replace the below line with your code logic 
  var dist=+userInput[0];

  console.log((dist*1000)+'\n'+(dist*100000));

  //end-here
});